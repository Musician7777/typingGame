export function generateWords(count = 50) {
  const words = [];
  for (let i = 0; i < count; i++) {
    words.push(commonWords[Math.floor(Math.random() * commonWords.length)]);
  }
  return words;
}

export function generateSentences(count = 3) {
  const selected = [];
  for (let i = 0; i < count; i++) {
    selected.push(sentences[Math.floor(Math.random() * sentences.length)]);
  }
  return selected.join(" ");
}

export function calculateWPM(charactersTyped, timeElapsed) {
  if (timeElapsed === 0) return 0;
  return Math.round(charactersTyped / 5 / (timeElapsed / 60));
}

export function calculateAccuracy(correct, total) {
  if (total === 0) return 100;
  return Math.round((correct / total) * 100);
}
